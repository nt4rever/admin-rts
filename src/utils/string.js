export function getFullName(firstName, lastName) {
  return [firstName, lastName].filter((value) => (value ?? null) !== null).join(" ");
}

export const slugify = (text) =>
  text
    .toString()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim()
    .replace(/\s+/g, "-")
    .replace(/[^\w-]+/g, "")
    .replace(/--+/g, "-");
